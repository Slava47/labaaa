        print("  - –ö–∞–∂–¥—ã–π —Ö–æ–¥: –≤—ã–±—Ä–∞—Ç—å –∫–ª–µ—Ç–∫—É –∏ —Å—ä–µ—Å—Ç—å –µ—ë –≤–º–µ—Å—Ç–µ —Å–æ –≤—Å–µ–º–∏ –ø—Ä–∞–≤–µ–µ –∏ –Ω–∏–∂–µ")
